/**************************************************************************************************
* 功能：任务执行
* 参数：无
* 描述：
**************************************************************************************************/
void Task_Process(void)
{
	unsigned char i;
    for(i = 0;i<TASK_MAX;i++)
    {
		if(TaskCompany[i].Run == TASK_RUN)
        {
			TaskCompany[i].Run = TASK_STOP;
			TaskCompany[i].TaskHook();
        }
    }
}
/**************************************************************************************************
* 功能：任务计数
* 参数：无
* 描述：1ms调用一次.
**************************************************************************************************/
void Task_Count(void)
{
	uint8_t i;
    for(i = 0;i<TASK_MAX;i++)
    {
		if(TaskCompany[i].Timer)
        {
			TaskCompany[i].Timer--;
			if(TaskCompany[i].Timer <= 0)
            {
				TaskCompany[i].Timer = TaskCompany[i].IvtTime;
                TaskCompany[i].Run = TASK_RUN;
            }
        }
    }
}
